    private void loginCheck(String username, String password){    
        try {
            Class.forName("com.mysql.jdbc.Driver");  
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/simp2kk", "root", "");
            String query = "SELECT * FROM login WHERE username = '" + username + "' AND password = '" + password + "' ";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(query);
           
            if (rs.next() == false) {
                JOptionPane.showMessageDialog(null, "Username atau Password tidak ditemukan");
                System.out.println("ResultSet in empty in Java");
                txtusername.setText("");
                txtpassword.setText("");
                
            // jika variabel id ada di database ditampilkan data yang ada di kolom inputan    
            } else {
                do {
                    // isi kolom ditulis sesuai return rs.getString(nama_kolom_di_database)
                    if (password.equals(rs.getString("password")) && username.equals(rs.getString("username"))) {
                    JOptionPane.showMessageDialog(null, "berhasil masuk");
                    new Dashboard().setVisible(true);
                    this.dispose();
                    }
                    
                // dijalankan selama rs.next true
                } while (rs.next());
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "TERJADI KESALAHAN");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

